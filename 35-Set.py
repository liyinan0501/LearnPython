# 不允许重复的情况下，把数据忘集合里面存储。

# 定义集合
# 方法1
s1 = {10, 20, 30}
print(s1)  # {10, 20, 30}
s2 = {10, 20, 30, 40, 50, 20}
print(s2)  # {50, 20, 40, 10, 30}
# ! 集合无顺序，不支持下标操作，不重复。

# 方法2
s3 = set("abcdefg")
print(s3)  # {'b', 'e', 'd', 'g', 'a', 'c', 'f'}

# 定义空集合
s4 = set()
print(s4)  # set()
print(type(s4))  # <class 'set'>
s5 = {}
print(type(s5))  # <class 'dict'>
# 定义空集合只能使用 set()，因为 {} 用来创建空字典，所以不要用 {} 来创建空集合。
